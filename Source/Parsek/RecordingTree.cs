    public class RecordingTree
    {
        internal const int CurrentTreeFormatVersion = 0;

        // --- Serialized fields ---
        public string Id;
        public string TreeName;                     // root vessel name at launch
        public string RootRecordingId;
        public string ActiveRecordingId;            // nullable: null if all background
        public string AutoGeneratedRootGroupName;
        public string AutoGeneratedDebrisGroupName;
        public string AutoGeneratedCrewGroupName;
        public int TreeFormatVersion;

        public Dictionary<string, Recording> Recordings
            = new Dictionary<string, Recording>();
        public List<BranchPoint> BranchPoints = new List<BranchPoint>();

        // --- Runtime-only fields (not serialized) ---
        // Maps vessel persistentId -> recordingId for background vessel lookup.
        // Rebuilt from Recordings on load via RebuildBackgroundMap().
        public Dictionary<uint, string> BackgroundMap
            = new Dictionary<uint, string>();

        // Set of all vessel PIDs that appear anywhere in this tree's recordings.
        // This is archived-history membership, not a globally unique ownership claim.
        // Used by ghost skip logic to answer the tree-local question
        // "does this PID appear in this tree at all?".
        // Rebuilt alongside BackgroundMap via RebuildBackgroundMap().
        public HashSet<uint> RecordedVesselPids = new HashSet<uint>();

        internal void AddOrReplaceRecording(Recording rec)
        {
            if (rec == null || string.IsNullOrEmpty(rec.RecordingId))
                return;

            AssignTreeOrderIfMissing(rec);
            Recordings[rec.RecordingId] = rec;
        }

        internal static RecordingTree DeepClone(RecordingTree source)
        {
            if (source == null)
                return null;

            var clone = new RecordingTree
            {
                Id = source.Id,
                TreeName = source.TreeName,
                RootRecordingId = source.RootRecordingId,
                ActiveRecordingId = source.ActiveRecordingId,
                AutoGeneratedRootGroupName = source.AutoGeneratedRootGroupName,
                AutoGeneratedDebrisGroupName = source.AutoGeneratedDebrisGroupName,
                AutoGeneratedCrewGroupName = source.AutoGeneratedCrewGroupName,
                TreeFormatVersion = source.TreeFormatVersion,
                BackgroundMap = source.BackgroundMap != null
                    ? new Dictionary<uint, string>(source.BackgroundMap)
                    : new Dictionary<uint, string>(),
                RecordedVesselPids = source.RecordedVesselPids != null
                    ? new HashSet<uint>(source.RecordedVesselPids)
                    : new HashSet<uint>()
            };

            if (source.Recordings != null)
            {
                foreach (var kvp in source.Recordings)
                {
                    Recording clonedRecording = Recording.DeepClone(kvp.Value);
                    if (clonedRecording != null)
                        clone.Recordings[kvp.Key] = clonedRecording;
                }
            }

            if (source.BranchPoints != null)
            {
                for (int i = 0; i < source.BranchPoints.Count; i++)
                    clone.BranchPoints.Add(DeepCloneBranchPoint(source.BranchPoints[i]));
            }

            return clone;
        }

        private static BranchPoint DeepCloneBranchPoint(BranchPoint source)
        {
            if (source == null)
                return null;

            return new BranchPoint
            {
                Id = source.Id,
                UT = source.UT,
                Type = source.Type,
                ParentRecordingIds = source.ParentRecordingIds != null
                    ? new List<string>(source.ParentRecordingIds)
                    : new List<string>(),
                ChildRecordingIds = source.ChildRecordingIds != null
                    ? new List<string>(source.ChildRecordingIds)
                    : new List<string>(),
                SplitCause = source.SplitCause,
                DecouplerPartId = source.DecouplerPartId,
                BreakupCause = source.BreakupCause,
                BreakupDuration = source.BreakupDuration,
                DebrisCount = source.DebrisCount,
                CoalesceWindow = source.CoalesceWindow,
                MergeCause = source.MergeCause,
                TargetVesselPersistentId = source.TargetVesselPersistentId,
                TerminalCause = source.TerminalCause,
                RewindPointId = source.RewindPointId
            };
        }

        /// <summary>
        /// Returns the maximum EndUT across all recordings in this tree.
        /// Returns 0 if all recordings have 0 points (degraded tree).
        /// </summary>
        internal double ComputeEndUT()
        {
            double endUT = 0;
            foreach (var rec in Recordings.Values)
            {
                double recEnd = rec.EndUT;
                if (recEnd > endUT) endUT = recEnd;
            }
            return endUT;
        }

        /// <summary>
        /// Returns true if this tree has no trajectory data (all recordings have 0 points).
        /// Degraded trees should not have their budget applied.
        /// </summary>
        internal bool IsDegraded => ComputeEndUT() == 0;

