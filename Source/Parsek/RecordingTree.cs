        public void Save(ConfigNode treeNode)
        {
            var ic = CultureInfo.InvariantCulture;

            treeNode.AddValue("id", Id ?? "");
            treeNode.AddValue("treeName", TreeName ?? "");
            treeNode.AddValue("rootRecordingId", RootRecordingId ?? "");
            TreeFormatVersion = CurrentTreeFormatVersion;
            treeNode.AddValue("treeFormatVersion", TreeFormatVersion.ToString(ic));
            treeNode.AddValue("recordingSchemaGeneration", RecordingStore.CurrentRecordingSchemaGeneration.ToString(ic));
            if (ActiveRecordingId != null)
                treeNode.AddValue("activeRecordingId", ActiveRecordingId);
            if (!string.IsNullOrEmpty(AutoGeneratedRootGroupName))
                treeNode.AddValue("autoGeneratedRootGroupName", AutoGeneratedRootGroupName);
            if (!string.IsNullOrEmpty(AutoGeneratedDebrisGroupName))
                treeNode.AddValue("autoGeneratedDebrisGroupName", AutoGeneratedDebrisGroupName);
            if (!string.IsNullOrEmpty(AutoGeneratedCrewGroupName))
                treeNode.AddValue("autoGeneratedCrewGroupName", AutoGeneratedCrewGroupName);

            // Phase F: legacy resource fields (preTreeFunds/preTreeScience/preTreeRep,
            // deltaFunds/deltaScience/deltaRep, resourcesApplied) are NOT written
            // anymore. They remain on the in-memory type so Phase A's load-time
            // migration in LedgerOrchestrator can still read pre-Phase-F .sfs files,
            // but the new save shape carries the ledger as the single source of truth.

            // Serialize each recording
            EnsureRecordingTreeOrder();
            var orderedRecordings = new List<Recording>(Recordings.Values);
            orderedRecordings.Sort(CompareRecordingTreeOrder);
            foreach (var rec in orderedRecordings)
            {
                ConfigNode recNode = treeNode.AddNode("RECORDING");
                SaveRecordingInto(recNode, rec);
            }

            // Serialize branch points
            for (int i = 0; i < BranchPoints.Count; i++)
            {
                ConfigNode bpNode = treeNode.AddNode("BRANCH_POINT");
                SaveBranchPointInto(bpNode, BranchPoints[i]);
            }

            // No per-tree log line here: this method is called once per committed tree
            // inside ParsekScenario.SaveTreeRecordings' loop, which would emit one line
            // per tree (hundreds with a large save). The caller logs a single batched
            // summary after the loop; the single-tree active/pending callers log their
            // own Info line.
        }

        public static RecordingTree Load(ConfigNode treeNode)
        {
            var ic = CultureInfo.InvariantCulture;

            var tree = new RecordingTree();
            tree.Id = treeNode.GetValue("id") ?? "";
            tree.TreeName = Recording.ResolveLocalizedName(treeNode.GetValue("treeName") ?? "");
            tree.RootRecordingId = treeNode.GetValue("rootRecordingId") ?? "";
            int treeFormatVersion;
            if (int.TryParse(treeNode.GetValue("treeFormatVersion"), NumberStyles.Integer, ic, out treeFormatVersion))
                tree.TreeFormatVersion = treeFormatVersion;
            else
                tree.TreeFormatVersion = 0;

            string schemaGenerationStr = treeNode.GetValue("recordingSchemaGeneration");
            if (!int.TryParse(schemaGenerationStr, NumberStyles.Integer, ic, out int treeSchemaGeneration)
                || treeSchemaGeneration != RecordingStore.CurrentRecordingSchemaGeneration)
            {
                ParsekLog.Warn("RecordingTree",
                    $"Load: skipped tree={tree.Id ?? "<no-tree>"} due to unsupported schema generation " +
                    $"generation={(schemaGenerationStr ?? "(missing)")} " +
                    $"expected={RecordingStore.CurrentRecordingSchemaGeneration.ToString(ic)}");
                tree.RootRecordingId = string.Empty;
                tree.ActiveRecordingId = null;
                tree.Recordings.Clear();
                tree.BranchPoints.Clear();
                return tree;
            }

            tree.ActiveRecordingId = treeNode.GetValue("activeRecordingId");
            tree.AutoGeneratedRootGroupName = treeNode.GetValue("autoGeneratedRootGroupName");
            tree.AutoGeneratedDebrisGroupName = treeNode.GetValue("autoGeneratedDebrisGroupName");
            tree.AutoGeneratedCrewGroupName = treeNode.GetValue("autoGeneratedCrewGroupName");

            // Load recordings
            HashSet<string> rejectedRecordingIds = null;
            ConfigNode[] recNodes = treeNode.GetNodes("RECORDING");
            for (int i = 0; i < recNodes.Length; i++)
            {
                var rec = new Recording();
                LoadRecordingFrom(recNodes[i], rec);
                if (rec.RecordingFormatVersion == -1)
                {
                    if (!string.IsNullOrEmpty(rec.RecordingId))
                    {
                        if (rejectedRecordingIds == null)
                            rejectedRecordingIds = new HashSet<string>(StringComparer.Ordinal);
                        rejectedRecordingIds.Add(rec.RecordingId);
                    }
                    ParsekLog.Warn("Codec",
                        $"RecordingTree.LoadFrom: skipped unsupported recording " +
                        $"id={rec.RecordingId ?? "<no-id>"} tree={tree.Id ?? "<no-tree>"}");
                    continue;
                }
                tree.AddOrReplaceRecording(rec);
            }

            // Load branch points
            ConfigNode[] bpNodes = treeNode.GetNodes("BRANCH_POINT");
            for (int i = 0; i < bpNodes.Length; i++)
            {
                tree.BranchPoints.Add(LoadBranchPointFrom(bpNodes[i]));
            }

            PruneRejectedRecordingReferences(tree, rejectedRecordingIds);
            tree.RebuildBackgroundMap();

            ParsekLog.Verbose("RecordingTree",
                $"Load: id={tree.Id} tree='{tree.TreeName}' recordings={tree.Recordings.Count} " +
                $"branchPoints={tree.BranchPoints.Count} treeFormatVersion={tree.TreeFormatVersion}");

            return tree;
        }

        private static void PruneRejectedRecordingReferences(
            RecordingTree tree,
            HashSet<string> rejectedRecordingIds)
        {
            if (tree == null || rejectedRecordingIds == null || rejectedRecordingIds.Count == 0)
                return;

            if (!string.IsNullOrEmpty(tree.RootRecordingId)
                && rejectedRecordingIds.Contains(tree.RootRecordingId))
            {
                int removedRecordings = tree.Recordings != null ? tree.Recordings.Count : 0;
                int rootRemovedBranchPoints = tree.BranchPoints != null ? tree.BranchPoints.Count : 0;
                string rootRecordingId = tree.RootRecordingId;
                tree.RootRecordingId = string.Empty;
                tree.ActiveRecordingId = null;
                if (tree.Recordings != null)
                    tree.Recordings.Clear();
                if (tree.BranchPoints != null)
                    tree.BranchPoints.Clear();
                if (tree.BackgroundMap != null)
                    tree.BackgroundMap.Clear();
                ParsekLog.Warn("RecordingTree",
                    $"Load: dropped entire tree id={tree.Id ?? "<no-tree>"} " +
                    $"because root recording '{rootRecordingId}' was rejected by schema gate; " +
                    $"removedRecordings={removedRecordings} removedBranchPoints={rootRemovedBranchPoints}");
                return;
            }

            if (!string.IsNullOrEmpty(tree.ActiveRecordingId)
                && rejectedRecordingIds.Contains(tree.ActiveRecordingId))
            {
                tree.ActiveRecordingId = null;
            }

            HashSet<string> removedBranchPointIds = null;
            int removedBranchPoints = 0;
            if (tree.BranchPoints != null)
            {
                for (int i = tree.BranchPoints.Count - 1; i >= 0; i--)
                {
                    BranchPoint bp = tree.BranchPoints[i];
                    bp.ParentRecordingIds?.RemoveAll(id => rejectedRecordingIds.Contains(id));
                    bp.ChildRecordingIds?.RemoveAll(id => rejectedRecordingIds.Contains(id));

                    int parentCount = bp.ParentRecordingIds != null ? bp.ParentRecordingIds.Count : 0;
                    int childCount = bp.ChildRecordingIds != null ? bp.ChildRecordingIds.Count : 0;
                    if (parentCount > 0 && childCount > 0)
                        continue;

                    if (removedBranchPointIds == null)
                        removedBranchPointIds = new HashSet<string>(StringComparer.Ordinal);
                    if (!string.IsNullOrEmpty(bp.Id))
                        removedBranchPointIds.Add(bp.Id);
                    tree.BranchPoints.RemoveAt(i);
                    removedBranchPoints++;
                }
            }

            foreach (Recording rec in tree.Recordings.Values)
            {
                ClearRejectedRecordingReferences(rec, rejectedRecordingIds, removedBranchPointIds);
            }

            ParsekLog.Warn("RecordingTree",
                $"Load: pruned references to {rejectedRecordingIds.Count} metadata-rejected recording(s) " +
                $"from tree={tree.Id ?? "<no-tree>"} removedBranchPoints={removedBranchPoints}");
        }

        /// <summary>
        /// Clears a single recording's references to schema-rejected recordings and removed
        /// branch points: nulls ParentRecordingId / ParentAnchorRecordingId when they point at a
        /// rejected recording id, and nulls ParentBranchPointId / ChildBranchPointId when they
        /// point at a removed branch point. Short-circuits on a null recording and on an empty
        /// removed-branch-point set. Mutates the recording.
        /// </summary>
        internal static void ClearRejectedRecordingReferences(
            Recording rec,
            HashSet<string> rejectedRecordingIds,
            HashSet<string> removedBranchPointIds)
        {
            if (rec == null)
                return;

            if (!string.IsNullOrEmpty(rec.ParentRecordingId)
                && rejectedRecordingIds.Contains(rec.ParentRecordingId))
            {
                rec.ParentRecordingId = null;
            }

            if (!string.IsNullOrEmpty(rec.ParentAnchorRecordingId)
                && rejectedRecordingIds.Contains(rec.ParentAnchorRecordingId))
            {
                rec.ParentAnchorRecordingId = null;
            }

            if (removedBranchPointIds == null || removedBranchPointIds.Count == 0)
                return;

            if (!string.IsNullOrEmpty(rec.ParentBranchPointId)
                && removedBranchPointIds.Contains(rec.ParentBranchPointId))
            {
                rec.ParentBranchPointId = null;
            }

            if (!string.IsNullOrEmpty(rec.ChildBranchPointId)
                && removedBranchPointIds.Contains(rec.ChildBranchPointId))
            {
                rec.ChildBranchPointId = null;
            }
        }

        public void RebuildBackgroundMap()
        {
            BackgroundMap.Clear();
            RecordedVesselPids.Clear();
            EnsureRecordingTreeOrder();
            var duplicateEligiblePids = new HashSet<uint>();
            var duplicateExamples = new List<string>();
            var orderedRecordings = new List<Recording>(Recordings.Values);
            orderedRecordings.Sort(CompareRecordingTreeOrder);
            int activePidSkips = 0;
            foreach (var rec in orderedRecordings)
            {
                if (rec.VesselPersistentId != 0)
                    RecordedVesselPids.Add(rec.VesselPersistentId);

                if (!string.Equals(rec.RecordingId, ActiveRecordingId, StringComparison.Ordinal)
                    && SharesVesselPidWithActiveRecording(rec))
                {
                    activePidSkips++;
                }

                if (IsBackgroundMapEligible(rec))
                {
                    if (BackgroundMap.TryGetValue(rec.VesselPersistentId, out string existingId))
                    {
                        duplicateEligiblePids.Add(rec.VesselPersistentId);

                        if (duplicateExamples.Count < 3)
                        {
                            duplicateExamples.Add(
                                $"{rec.VesselPersistentId}:{existingId}->{rec.RecordingId}");
                        }
                    }

                    BackgroundMap[rec.VesselPersistentId] = rec.RecordingId;
                }
            }

            if (duplicateEligiblePids.Count > 0)
            {
                string treeId = !string.IsNullOrEmpty(Id) ? Id : (TreeName ?? "unknown");
                string exampleSuffix = duplicateExamples.Count > 0
                    ? $" examples=[{string.Join(", ", duplicateExamples)}]"
                    : string.Empty;
                ParsekLog.VerboseRateLimited(
                    "RecordingTree",
                    $"bgmap-duplicate-{treeId}",
                    $"RebuildBackgroundMap: tree='{treeId}' has {duplicateEligiblePids.Count} " +
                    $"duplicate eligible PID(s); BackgroundMap kept the latest tree-order winner." +
                    exampleSuffix,
                    60.0);
            }

            ParsekLog.Verbose("RecordingTree",
                $"RebuildBackgroundMap: entries={BackgroundMap.Count} recordedPids={RecordedVesselPids.Count} totalRecordings={Recordings.Count} activePidSkips={activePidSkips}");
        }

        internal bool IsBackgroundMapEligible(Recording rec)
        {
            return rec != null
                && rec.VesselPersistentId != 0
                && rec.TerminalStateValue == null
                && !HasNextChainSegment(rec)
                && rec.ChildBranchPointId == null
                && rec.RecordingId != ActiveRecordingId
                && !SharesVesselPidWithActiveRecording(rec);
        }

        private bool SharesVesselPidWithActiveRecording(Recording rec)
        {
            if (rec == null || rec.VesselPersistentId == 0)
                return false;
            if (string.IsNullOrEmpty(ActiveRecordingId))
                return false;
            if (Recordings == null
                || !Recordings.TryGetValue(ActiveRecordingId, out Recording activeRec)
                || activeRec == null
                || activeRec.VesselPersistentId == 0)
            {
                return false;
            }

            return rec.VesselPersistentId == activeRec.VesselPersistentId;
        }

        private bool HasNextChainSegment(Recording rec)
        {
            if (rec == null || string.IsNullOrEmpty(rec.ChainId))
                return false;

            int nextIdx = rec.ChainIndex + 1;
            foreach (var other in Recordings.Values)
            {
                if (other.ChainId == rec.ChainId
                    && other.ChainIndex == nextIdx
                    && other.ChainBranch == rec.ChainBranch)
                {
                    return true;
                }
            }

            return false;
        }

        private void EnsureRecordingTreeOrder()
        {
            int nextOrder = 0;
            foreach (var rec in Recordings.Values)
            {
                if (rec != null && rec.TreeOrder >= nextOrder)
                    nextOrder = rec.TreeOrder + 1;
            }

            foreach (var rec in Recordings.Values)
            {
                if (rec != null && rec.TreeOrder < 0)
                    rec.TreeOrder = nextOrder++;
            }
        }

        private void AssignTreeOrderIfMissing(Recording rec)
        {
            if (rec == null || rec.TreeOrder >= 0)
                return;

            int nextOrder = 0;
            foreach (var existing in Recordings.Values)
            {
                if (existing != null && existing.TreeOrder >= nextOrder)
                    nextOrder = existing.TreeOrder + 1;
            }

            rec.TreeOrder = nextOrder;
        }

        private static int CompareRecordingTreeOrder(Recording a, Recording b)
        {
            if (ReferenceEquals(a, b))
                return 0;
            if (a == null)
                return -1;
            if (b == null)
                return 1;

            int aOrder = a.TreeOrder >= 0 ? a.TreeOrder : int.MaxValue;
            int bOrder = b.TreeOrder >= 0 ? b.TreeOrder : int.MaxValue;
            int cmp = aOrder.CompareTo(bOrder);
            if (cmp != 0)
                return cmp;

            cmp = a.StartUT.CompareTo(b.StartUT);
            if (cmp != 0)
                return cmp;

            cmp = a.EndUT.CompareTo(b.EndUT);
            if (cmp != 0)
                return cmp;

            return string.CompareOrdinal(a.RecordingId, b.RecordingId);
        }

        internal List<uint> FindDuplicateBackgroundMapPids()
        {
            var counts = new Dictionary<uint, int>();
            var duplicates = new List<uint>();

            foreach (var rec in Recordings.Values)
            {
                if (!IsBackgroundMapEligible(rec))
                    continue;

                int nextCount = 1;
                if (counts.TryGetValue(rec.VesselPersistentId, out int existing))
                    nextCount = existing + 1;

                counts[rec.VesselPersistentId] = nextCount;
                if (nextCount == 2)
                    duplicates.Add(rec.VesselPersistentId);
            }

            return duplicates;
        }
