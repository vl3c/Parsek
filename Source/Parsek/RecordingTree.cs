        internal static void SaveBranchPointInto(ConfigNode bpNode, BranchPoint bp)
        {
            var ic = CultureInfo.InvariantCulture;

            bpNode.AddValue("id", bp.Id ?? "");
            bpNode.AddValue("ut", bp.UT.ToString("R", ic));
            bpNode.AddValue("type", ((int)bp.Type).ToString(ic));

            for (int i = 0; i < bp.ParentRecordingIds.Count; i++)
                bpNode.AddValue("parentId", bp.ParentRecordingIds[i] ?? "");

            for (int i = 0; i < bp.ChildRecordingIds.Count; i++)
                bpNode.AddValue("childId", bp.ChildRecordingIds[i] ?? "");

            // SPLIT metadata
            if (bp.SplitCause != null)
                bpNode.AddValue("splitCause", bp.SplitCause);
            if (bp.DecouplerPartId != 0)
                bpNode.AddValue("decouplerPartId", bp.DecouplerPartId.ToString(ic));

            // BREAKUP metadata
            if (bp.BreakupCause != null)
                bpNode.AddValue("breakupCause", bp.BreakupCause);
            if (bp.BreakupDuration != 0)
                bpNode.AddValue("breakupDuration", bp.BreakupDuration.ToString("R", ic));
            if (bp.DebrisCount != 0)
                bpNode.AddValue("debrisCount", bp.DebrisCount.ToString(ic));
            if (bp.CoalesceWindow != 0)
                bpNode.AddValue("coalesceWindow", bp.CoalesceWindow.ToString("R", ic));

            // MERGE metadata
            if (bp.MergeCause != null)
                bpNode.AddValue("mergeCause", bp.MergeCause);
            if (bp.TargetVesselPersistentId != 0)
                bpNode.AddValue("targetVesselPid", bp.TargetVesselPersistentId.ToString(ic));

            // TERMINAL metadata
            if (bp.TerminalCause != null)
                bpNode.AddValue("terminalCause", bp.TerminalCause);

            // Rewind-to-Staging (design section 5.4)
            if (!string.IsNullOrEmpty(bp.RewindPointId))
                bpNode.AddValue("rewindPointId", bp.RewindPointId);

            ParsekLog.Verbose("RecordingTree",
                $"SaveBranchPoint: id={bp.Id} type={bp.Type} ut={bp.UT.ToString("F1", ic)}" +
                (bp.SplitCause != null ? $" splitCause={bp.SplitCause}" : "") +
                (bp.DecouplerPartId != 0 ? $" decouplerPartId={bp.DecouplerPartId}" : "") +
                (bp.BreakupCause != null ? $" breakupCause={bp.BreakupCause}" : "") +
                (bp.BreakupDuration != 0 ? $" breakupDuration={bp.BreakupDuration.ToString("F3", ic)}" : "") +
                (bp.DebrisCount != 0 ? $" debrisCount={bp.DebrisCount}" : "") +
                (bp.CoalesceWindow != 0 ? $" coalesceWindow={bp.CoalesceWindow.ToString("F3", ic)}" : "") +
                (bp.MergeCause != null ? $" mergeCause={bp.MergeCause}" : "") +
                (bp.TargetVesselPersistentId != 0 ? $" targetVesselPid={bp.TargetVesselPersistentId}" : "") +
                (bp.TerminalCause != null ? $" terminalCause={bp.TerminalCause}" : ""));
        }

        internal static BranchPoint LoadBranchPointFrom(ConfigNode bpNode)
        {
            var inv = NumberStyles.Float;
            var ic = CultureInfo.InvariantCulture;

            var bp = new BranchPoint();
            bp.Id = bpNode.GetValue("id") ?? "";
            double.TryParse(bpNode.GetValue("ut"), inv, ic, out bp.UT);

            int typeInt;
            if (int.TryParse(bpNode.GetValue("type"), NumberStyles.Integer, ic, out typeInt))
            {
                if (Enum.IsDefined(typeof(BranchPointType), typeInt))
                {
                    bp.Type = (BranchPointType)typeInt;
                }
                else
                {
                    ParsekLog.Warn("RecordingTree",
                        $"LoadBranchPoint: unknown type integer {typeInt} for id={bp.Id}, defaulting to Undock(0)");
                    bp.Type = BranchPointType.Undock;
                }
            }

            string[] parentIds = bpNode.GetValues("parentId");
            bp.ParentRecordingIds = new List<string>(parentIds);

            string[] childIds = bpNode.GetValues("childId");
            bp.ChildRecordingIds = new List<string>(childIds);

            // SPLIT metadata
            bp.SplitCause = bpNode.GetValue("splitCause");
            string decouplerPartIdStr = bpNode.GetValue("decouplerPartId");
            if (decouplerPartIdStr != null)
            {
                uint decouplerPartId;
                if (uint.TryParse(decouplerPartIdStr, NumberStyles.Integer, ic, out decouplerPartId))
                    bp.DecouplerPartId = decouplerPartId;
            }

            // BREAKUP metadata
            bp.BreakupCause = bpNode.GetValue("breakupCause");
            string breakupDurationStr = bpNode.GetValue("breakupDuration");
            if (breakupDurationStr != null)
            {
                double breakupDuration;
                if (double.TryParse(breakupDurationStr, inv, ic, out breakupDuration))
                    bp.BreakupDuration = breakupDuration;
            }
            string debrisCountStr = bpNode.GetValue("debrisCount");
            if (debrisCountStr != null)
            {
                int debrisCount;
                if (int.TryParse(debrisCountStr, NumberStyles.Integer, ic, out debrisCount))
                    bp.DebrisCount = debrisCount;
            }
            string coalesceWindowStr = bpNode.GetValue("coalesceWindow");
            if (coalesceWindowStr != null)
            {
                double coalesceWindow;
                if (double.TryParse(coalesceWindowStr, inv, ic, out coalesceWindow))
                    bp.CoalesceWindow = coalesceWindow;
            }

            // MERGE metadata
            bp.MergeCause = bpNode.GetValue("mergeCause");
            string targetVesselPidStr = bpNode.GetValue("targetVesselPid");
            if (targetVesselPidStr != null)
            {
                uint targetVesselPid;
                if (uint.TryParse(targetVesselPidStr, NumberStyles.Integer, ic, out targetVesselPid))
                    bp.TargetVesselPersistentId = targetVesselPid;
            }

            // TERMINAL metadata
            bp.TerminalCause = bpNode.GetValue("terminalCause");

            // Rewind-to-Staging (design section 5.4). Absent -> null.
            string rpId = bpNode.GetValue("rewindPointId");
            bp.RewindPointId = string.IsNullOrEmpty(rpId) ? null : rpId;

            ParsekLog.Verbose("RecordingTree",
                $"LoadBranchPoint: id={bp.Id} type={bp.Type} ut={bp.UT.ToString("F1", ic)}" +
                (bp.SplitCause != null ? $" splitCause={bp.SplitCause}" : "") +
                (bp.DecouplerPartId != 0 ? $" decouplerPartId={bp.DecouplerPartId}" : "") +
                (bp.BreakupCause != null ? $" breakupCause={bp.BreakupCause}" : "") +
                (bp.BreakupDuration != 0 ? $" breakupDuration={bp.BreakupDuration.ToString("F3", ic)}" : "") +
                (bp.DebrisCount != 0 ? $" debrisCount={bp.DebrisCount}" : "") +
                (bp.CoalesceWindow != 0 ? $" coalesceWindow={bp.CoalesceWindow.ToString("F3", ic)}" : "") +
                (bp.MergeCause != null ? $" mergeCause={bp.MergeCause}" : "") +
                (bp.TargetVesselPersistentId != 0 ? $" targetVesselPid={bp.TargetVesselPersistentId}" : "") +
                (bp.TerminalCause != null ? $" terminalCause={bp.TerminalCause}" : ""));

            return bp;
        }

        // --- Leaf identification ---

        /// <summary>
        /// Identifies spawnable leaf recordings: no children, not terminal
        /// (Destroyed/Recovered/Docked/Boarded), has vessel snapshot.
        /// </summary>
        public List<Recording> GetSpawnableLeaves()
        {
            var leaves = new List<Recording>();
            foreach (var rec in Recordings.Values)
            {
                if (IsSpawnableLeaf(rec))
                    leaves.Add(rec);
            }
            return leaves;
        }

        /// <summary>
        /// Identifies ALL leaf recordings (including destroyed/recovered).
        /// A leaf is any recording with ChildBranchPointId == null.
        /// </summary>
        public List<Recording> GetAllLeaves()
        {
            var leaves = new List<Recording>();
            foreach (var rec in Recordings.Values)
            {
                if (rec.ChildBranchPointId == null)
                    leaves.Add(rec);
            }
            return leaves;
        }

        /// <summary>
        /// Checks whether a recording is a spawnable leaf:
        /// 1. No children (ChildBranchPointId is null)
        /// 2. Terminal state allows spawning (not Destroyed/Recovered/Docked/Boarded)
        /// 3. Has a vessel snapshot
        /// </summary>
        internal static bool IsSpawnableLeaf(Recording rec)
        {
            if (rec.ChildBranchPointId != null)
                return false;

            if (rec.TerminalStateValue.HasValue)
            {
                var ts = rec.TerminalStateValue.Value;
                if (ts == TerminalState.Destroyed || ts == TerminalState.Recovered
                    || ts == TerminalState.Docked || ts == TerminalState.Boarded)
                    return false;
            }

            if (rec.VesselSnapshot == null)
                return false;

            return true;
        }

        /// <summary>
        /// Assigns terminal state based on vessel situation.
        /// Pure static method for testability.
        /// </summary>
        internal static TerminalState DetermineTerminalState(int situation)
        {
            // Vessel.Situations enum values: LANDED=1, SPLASHED=2, PRELAUNCH=4,
            // FLYING=8, SUB_ORBITAL=16, ORBITING=32, ESCAPING=64, DOCKED=128
            switch (situation)
            {
                case 32: // ORBITING
                    return TerminalState.Orbiting;
                case 1: // LANDED
                    return TerminalState.Landed;
                case 2: // SPLASHED
                    return TerminalState.Splashed;
                case 16: // SUB_ORBITAL
                case 8:  // FLYING
                case 64: // ESCAPING
                    return TerminalState.SubOrbital;
                case 4:  // PRELAUNCH
                    return TerminalState.Landed;
                case 128: // DOCKED
                    return TerminalState.Docked;
                default:
                    ParsekLog.Warn("RecordingTree", $"DetermineTerminalState: unexpected situation={situation}, defaulting to SubOrbital");
                    return TerminalState.SubOrbital;
            }
        }

        /// <summary>
        /// Pure decision: is the orbit a stable bound orbit whose periapsis clears
        /// the body's atmosphere (or surface, if the body has no atmosphere)?
        /// Atmospheric grazers — orbits whose periapsis radius lies inside
        /// <paramref name="atmosphereDepth"/> — are excluded because they decay
        /// to destruction within a few orbits via drag. Strict inequality at the
        /// atmosphere top mirrors KSP's drag boundary.
        /// </summary>
        internal static bool IsBoundOrbitAboveAtmosphere(
            double eccentricity,
            double periapsisRadius,
            double bodyRadius,
            bool bodyHasAtmosphere,
            double atmosphereDepth)
        {
            if (!IsFinite(eccentricity)
                || !IsFinite(periapsisRadius)
                || !IsFinite(bodyRadius)
                || bodyRadius <= 0.0)
            {
                return false;
            }

            if (eccentricity >= 1.0)
                return false;

            double effectiveAtmosphereDepth = bodyHasAtmosphere
                && IsFinite(atmosphereDepth)
                && atmosphereDepth > 0.0
                    ? atmosphereDepth
                    : 0.0;
            double minSafePeR = bodyRadius + effectiveAtmosphereDepth;
            return periapsisRadius > minSafePeR;
        }

        /// <summary>
        /// Assigns terminal state based on vessel situation, with orbit-aware override.
        /// KSP can report SUB_ORBITAL for a stable bound orbit, and ORBITING for
        /// a ballistic coast whose periapsis is inside the surface or atmosphere.
        /// This overload checks the vessel's actual orbit to correct both cases.
        /// </summary>
        internal static TerminalState DetermineTerminalState(int situation, Vessel vessel)
        {
            TerminalState baseState = DetermineTerminalState(situation);
            if (vessel?.orbit == null || vessel.orbit.referenceBody == null)
                return baseState;

            var body = vessel.orbit.referenceBody;
            bool bodyHasAtmosphere = body.atmosphere;
            double atmosphereDepth = bodyHasAtmosphere ? body.atmosphereDepth : 0.0;
            TerminalState resolved = DetermineTerminalStateFromOrbitEvidence(
                situation,
                vessel.orbit.eccentricity,
                vessel.orbit.PeR,
                body.Radius,
                bodyHasAtmosphere,
                atmosphereDepth);

            if (baseState == TerminalState.SubOrbital && resolved == TerminalState.Orbiting)
            {
                ParsekLog.Info("RecordingTree",
                    $"DetermineTerminalState: overriding SUB_ORBITAL to Orbiting - vessel has bound orbit " +
                    $"(ecc={vessel.orbit.eccentricity:F4}, PeR={vessel.orbit.PeR:F0}, " +
                    $"bodyR={body.Radius:F0}, atmoTop={atmosphereDepth:F0}, " +
                    $"bodyHasAtmosphere={bodyHasAtmosphere})");
            }
            else if (baseState == TerminalState.Orbiting && resolved == TerminalState.SubOrbital)
            {
                string reason = vessel.orbit.eccentricity >= 1.0
                    ? "orbit evidence is unbound"
                    : vessel.orbit.PeR <= body.Radius
                        ? "periapsis is below surface"
                        : "periapsis is inside atmosphere";
                ParsekLog.Info("RecordingTree",
                    $"DetermineTerminalState: overriding ORBITING to SubOrbital - {reason} " +
                    $"(ecc={vessel.orbit.eccentricity:F4}, PeR={vessel.orbit.PeR:F0}, " +
                    $"bodyR={body.Radius:F0}, atmoTop={atmosphereDepth:F0}, " +
                    $"bodyHasAtmosphere={bodyHasAtmosphere})");
            }

            return resolved;
        }

        internal static TerminalState DetermineTerminalStateFromOrbitEvidence(
            int situation,
            double eccentricity,
            double periapsisRadius,
            double bodyRadius,
            bool bodyHasAtmosphere = false,
            double atmosphereDepth = 0.0)
        {
            TerminalState baseState = DetermineTerminalState(situation);
            if (!IsFinite(eccentricity)
                || !IsFinite(periapsisRadius)
                || !IsFinite(bodyRadius)
                || bodyRadius <= 0.0)
            {
                return baseState;
            }

            if (baseState == TerminalState.SubOrbital
                && situation == 16
                && IsBoundOrbitAboveAtmosphere(
                    eccentricity,
                    periapsisRadius,
                    bodyRadius,
                    bodyHasAtmosphere,
                    atmosphereDepth))
            {
                return TerminalState.Orbiting;
            }

            if (baseState == TerminalState.Orbiting
                && situation == 32
                && !IsBoundOrbitAboveAtmosphere(
                    eccentricity,
                    periapsisRadius,
                    bodyRadius,
                    bodyHasAtmosphere,
                    atmosphereDepth))
            {
                return TerminalState.SubOrbital;
            }

            return baseState;
        }

        private static bool IsFinite(double value)
        {
            return !double.IsNaN(value) && !double.IsInfinity(value);
        }

        static bool IsNonSpawnableTerminal(TerminalState terminalState)
        {
            return terminalState == TerminalState.Destroyed
                || terminalState == TerminalState.Recovered
                || terminalState == TerminalState.Docked
                || terminalState == TerminalState.Boarded;
        }

        /// <summary>
        /// Pure decision method: checks whether all leaf recordings in a tree have
        /// non-spawnable terminal states (Destroyed, Recovered, Docked, Boarded).
        /// A recording is a leaf if it has no ChildBranchPointId.
        /// Leaves with null TerminalStateValue are considered NOT terminal (still active).
        /// If activeRecordingId is non-null, the active recording is treated as alive
        /// unless activeVesselDestroyed is true.
        /// </summary>
        internal static bool AreAllLeavesTerminal(
            Dictionary<string, Recording> recordings,
            string activeRecordingId,
            bool activeVesselDestroyed)
        {
            int total = 0;
            int terminalCount = 0;
            int aliveCount = 0;
            bool result = true;

            // Deliberately scan every leaf here: this is a destruction merge gate,
            // and the log summary is useful only when its counts describe the
            // whole tree. The all-terminal path had to inspect every leaf before
            // this log-hygiene change as well.
            foreach (var kvp in recordings)
            {
                var rec = kvp.Value;

                // Skip non-leaf recordings (they branched into children)
                if (rec.ChildBranchPointId != null)
                    continue;

                total++;
                bool isActiveRecording = activeRecordingId != null && rec.RecordingId == activeRecordingId;
                bool isTerminal;

                // Leaf: active recording still alive — tree is not fully terminal
                if (isActiveRecording && !activeVesselDestroyed)
                {
                    isTerminal = false;
                }
                // Active recording with destroyed vessel: treat as terminal even if
                // TerminalStateValue hasn't been set yet (recorder knows it's dead,
                // but FinalizeTreeRecordings hasn't run to set the terminal state)
                else if (isActiveRecording && activeVesselDestroyed)
                {
                    isTerminal = true;
                }
                // Leaf: no terminal state means still recording / not finalized
                else if (!rec.TerminalStateValue.HasValue)
                {
                    isTerminal = false;
                }
                else
                {
                    var ts = rec.TerminalStateValue.Value;
                    isTerminal = IsNonSpawnableTerminal(ts);
                }

                if (isTerminal)
                {
                    terminalCount++;
                }
                else
                {
                    aliveCount++;
                    result = false;
                }
            }

            ParsekLog.Verbose("TreeDestruction",
                $"AreAllLeavesTerminal: leaves={total} terminal={terminalCount} " +
                $"alive={aliveCount} -> {result}");
            return result;
        }

        /// <summary>
        /// Returns true when the only leaves still blocking same-scene crash finalization
        /// are debris leaves. This is used only to decide whether the active-crash flow
        /// should keep waiting for a debris-only fallback owner; it does NOT make the tree
        /// merge-ready on its own.
        /// </summary>
        internal static bool AreAllActiveCrashBlockersDebris(
            Dictionary<string, Recording> recordings,
            string activeRecordingId)
        {
            bool sawBlockingLeaf = false;

            foreach (var kvp in recordings)
            {
                var rec = kvp.Value;
                if (rec.ChildBranchPointId != null)
                    continue;

                bool isActiveRecording = activeRecordingId != null && rec.RecordingId == activeRecordingId;
                if (isActiveRecording)
                    continue;

                if (!rec.TerminalStateValue.HasValue)
                {
                    sawBlockingLeaf = true;
                    if (!rec.IsDebris)
                        return false;
                    continue;
                }

                if (IsNonSpawnableTerminal(rec.TerminalStateValue.Value))
                    continue;

                sawBlockingLeaf = true;
                if (!rec.IsDebris)
                    return false;
            }

            return sawBlockingLeaf;
        }

